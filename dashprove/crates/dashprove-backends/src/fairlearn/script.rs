//! Fairlearn script generation and output parsing

use super::config::FairlearnConfig;
use crate::counterexample::{build_fairlearn_counterexample, StructuredCounterexample};
use crate::traits::{BackendError, VerificationStatus};
use dashprove_usl::typecheck::TypedSpec;

/// Generate a Fairlearn assessment script
pub fn generate_fairlearn_script(
    _spec: &TypedSpec,
    config: &FairlearnConfig,
) -> Result<String, BackendError> {
    let fairness_metric = config.fairness_metric.as_str();
    let mitigation_method = config.mitigation_method.as_str();
    let fairness_threshold = config.fairness_threshold;
    let n_samples = config.n_samples;

    Ok(format!(
        r#"#!/usr/bin/env python3
"""
Fairlearn fairness assessment script
Generated by DashProve

Evaluates model fairness using Fairlearn metrics and mitigation.
"""

import sys
import json
import time
import numpy as np

try:
    import fairlearn
    from fairlearn.metrics import (
        MetricFrame,
        demographic_parity_difference,
        equalized_odds_difference,
        selection_rate,
        true_positive_rate,
        false_positive_rate,
    )
except ImportError as e:
    print(f"FAIRLEARN_ERROR: Missing Fairlearn: {{e}}")
    print("FAIRLEARN_ERROR: Install with: pip install fairlearn")
    sys.exit(1)

try:
    import pandas as pd
    from sklearn.ensemble import RandomForestClassifier
    from sklearn.model_selection import train_test_split
    from sklearn.metrics import accuracy_score
except ImportError as e:
    print(f"FAIRLEARN_ERROR: Dependencies required: {{e}}")
    sys.exit(1)


def create_test_data(n_samples):
    """Create test data with sensitive attribute."""
    np.random.seed(42)

    # Features
    X = pd.DataFrame({{
        "feature_1": np.random.normal(0, 1, n_samples),
        "feature_2": np.random.uniform(0, 10, n_samples),
        "feature_3": np.random.exponential(1, n_samples),
    }})

    # Sensitive attribute (2 groups)
    sensitive = np.random.choice(["group_A", "group_B"], n_samples, p=[0.6, 0.4])

    # Target with some correlation to features
    y = ((X["feature_1"] + X["feature_2"] > 5) & (np.random.random(n_samples) > 0.2)).astype(int)

    return X, y, sensitive


def main():
    fairness_metric = "{fairness_metric}"
    mitigation_method = "{mitigation_method}"
    fairness_threshold = {fairness_threshold}
    n_samples = {n_samples}

    # Create test data
    X, y, sensitive = create_test_data(n_samples)

    # Split data
    X_train, X_test, y_train, y_test, sens_train, sens_test = train_test_split(
        X, y, sensitive, test_size=0.3, random_state=42
    )

    start_time = time.perf_counter()

    try:
        # Train model
        model = RandomForestClassifier(n_estimators=50, random_state=42)
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        # Calculate accuracy
        accuracy = accuracy_score(y_test, y_pred)

        # Calculate fairness metrics using MetricFrame
        metric_frame = MetricFrame(
            metrics={{
                "selection_rate": selection_rate,
                "accuracy": accuracy_score,
            }},
            y_true=y_test,
            y_pred=y_pred,
            sensitive_features=sens_test
        )

        # Get group-level metrics
        by_group = metric_frame.by_group.to_dict()

        # Calculate key fairness metrics
        dp_diff = demographic_parity_difference(
            y_test, y_pred, sensitive_features=sens_test
        )

        # Calculate equalized odds components
        try:
            eq_odds_diff = equalized_odds_difference(
                y_test, y_pred, sensitive_features=sens_test
            )
        except Exception:
            eq_odds_diff = None

        # Determine fairness based on metric
        if fairness_metric == "demographic_parity":
            primary_metric_value = dp_diff
        elif fairness_metric == "equalized_odds" and eq_odds_diff is not None:
            primary_metric_value = eq_odds_diff
        else:
            primary_metric_value = dp_diff

        is_fair = abs(primary_metric_value) <= fairness_threshold

        assessment_time = time.perf_counter() - start_time

        status = "success"
        error_message = None

    except Exception as e:
        status = "error"
        error_message = str(e)
        assessment_time = time.perf_counter() - start_time
        dp_diff = None
        eq_odds_diff = None
        primary_metric_value = None
        is_fair = False
        accuracy = 0.0
        by_group = {{}}

    print("FAIRLEARN_RESULT_START")
    result = {{
        "status": status,
        "fairness_metric": fairness_metric,
        "mitigation_method": mitigation_method,
        "n_samples": n_samples,
        "train_samples": len(X_train) if status == "success" else 0,
        "test_samples": len(X_test) if status == "success" else 0,
        "accuracy": accuracy,
        "demographic_parity_diff": dp_diff,
        "equalized_odds_diff": eq_odds_diff,
        "primary_metric_value": primary_metric_value,
        "fairness_threshold": fairness_threshold,
        "is_fair": is_fair,
        "by_group_metrics": by_group,
        "assessment_time_s": assessment_time,
        "fairlearn_version": fairlearn.__version__,
        "error": error_message
    }}
    print(json.dumps(result, indent=2, default=str))
    print("FAIRLEARN_RESULT_END")

    if status == "success":
        print(f"\nFAIRLEARN_SUMMARY: Fairness metric: {{fairness_metric}}")
        print(f"FAIRLEARN_SUMMARY: Primary metric value: {{primary_metric_value:.4f}}")
        print(f"FAIRLEARN_SUMMARY: Threshold: {{fairness_threshold}}")
        print(f"FAIRLEARN_SUMMARY: Model accuracy: {{accuracy:.2%}}")
        print(f"FAIRLEARN_SUMMARY: Is fair: {{is_fair}}")

        if is_fair:
            print("FAIRLEARN_STATUS: VERIFIED")
        elif abs(primary_metric_value) <= fairness_threshold * 2:
            print("FAIRLEARN_STATUS: PARTIALLY_VERIFIED")
        else:
            print("FAIRLEARN_STATUS: NOT_VERIFIED")
    else:
        print(f"FAIRLEARN_ERROR: {{error_message}}")


if __name__ == "__main__":
    main()
"#
    ))
}

/// Parse Fairlearn output
pub fn parse_fairlearn_output(
    stdout: &str,
    stderr: &str,
) -> (VerificationStatus, Option<StructuredCounterexample>) {
    if stdout.contains("FAIRLEARN_ERROR:") || stderr.contains("FAIRLEARN_ERROR:") {
        let error_msg = stdout
            .lines()
            .chain(stderr.lines())
            .find(|l| l.contains("FAIRLEARN_ERROR:"))
            .map(|l| l.replace("FAIRLEARN_ERROR:", "").trim().to_string())
            .unwrap_or_else(|| "Unknown Fairlearn error".to_string());
        return (VerificationStatus::Unknown { reason: error_msg }, None);
    }

    if let Some(json_str) =
        extract_json_result(stdout, "FAIRLEARN_RESULT_START", "FAIRLEARN_RESULT_END")
    {
        if let Ok(result) = serde_json::from_str::<serde_json::Value>(&json_str) {
            let status = result["status"].as_str().unwrap_or("error");
            let is_fair = result["is_fair"].as_bool().unwrap_or(false);
            let primary_metric = result["primary_metric_value"].as_f64().unwrap_or(1.0);
            let threshold = result["fairness_threshold"].as_f64().unwrap_or(0.1);

            if status == "error" {
                let error = result["error"]
                    .as_str()
                    .unwrap_or("Unknown error")
                    .to_string();
                return (VerificationStatus::Unknown { reason: error }, None);
            }

            // Use the shared fairness counterexample helper with populated failed_checks
            let counterexample = build_fairlearn_counterexample(&result);

            if is_fair {
                return (VerificationStatus::Proven, None);
            } else if primary_metric.abs() <= threshold * 2.0 {
                return (
                    VerificationStatus::Partial {
                        verified_percentage: (1.0 - primary_metric.abs() / (threshold * 2.0))
                            * 100.0,
                    },
                    counterexample,
                );
            } else {
                return (VerificationStatus::Disproven, counterexample);
            }
        }
    }

    if stdout.contains("FAIRLEARN_STATUS: VERIFIED") {
        (VerificationStatus::Proven, None)
    } else if stdout.contains("FAIRLEARN_STATUS: PARTIALLY_VERIFIED") {
        (
            VerificationStatus::Partial {
                verified_percentage: 75.0,
            },
            None,
        )
    } else if stdout.contains("FAIRLEARN_STATUS: NOT_VERIFIED") {
        (VerificationStatus::Disproven, None)
    } else {
        (
            VerificationStatus::Unknown {
                reason: "Could not parse Fairlearn output".to_string(),
            },
            None,
        )
    }
}

fn extract_json_result(output: &str, start_marker: &str, end_marker: &str) -> Option<String> {
    if let Some(start) = output.find(start_marker) {
        let after_start = &output[start + start_marker.len()..];
        if let Some(end) = after_start.find(end_marker) {
            return Some(after_start[..end].trim().to_string());
        }
    }
    None
}
